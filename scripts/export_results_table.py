from __future__ import annotations

import argparse
import csv
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional


ASPECTS = ["battery", "camera", "performance", "design", "price", "service"]


@dataclass
class EvalResult:
    name: str
    macro_f1: Optional[float] = None
    micro_f1: Optional[float] = None
    sent_acc: Optional[float] = None
    sent_n: Optional[int] = None
    per_aspect_f1: Dict[str, float] = None

    def __post_init__(self):
        if self.per_aspect_f1 is None:
            self.per_aspect_f1 = {}


_FLOAT = r"[-+]?\d*\.\d+|\d+"


def _read_text(p: Path) -> str:
    return p.read_text(encoding="utf-8", errors="ignore")


def _find_float(pattern: str, text: str) -> Optional[float]:
    m = re.search(pattern, text, flags=re.IGNORECASE)
    if not m:
        return None
    try:
        return float(m.group(1))
    except Exception:
        return None


def _find_int(pattern: str, text: str) -> Optional[int]:
    m = re.search(pattern, text, flags=re.IGNORECASE)
    if not m:
        return None
    try:
        return int(m.group(1))
    except Exception:
        return None


def parse_eval_log(model_name: str, log_path: Path) -> EvalResult:
    """
    Supports logs like:
      macro_f1 : 0.8150
      micro_f1 : 0.9272
      sent_acc : 0.8766 (n=3897)
      - battery    : 0.9654
      ...
    """
    text = _read_text(log_path)

    r = EvalResult(name=model_name)

    r.macro_f1 = _find_float(rf"macro_f1\s*:\s*({_FLOAT})", text)
    r.micro_f1 = _find_float(rf"micro_f1\s*:\s*({_FLOAT})", text)

    # sent_acc may not exist for baseline/single
    r.sent_acc = _find_float(rf"sent_acc\s*:\s*({_FLOAT})", text)

    # FIX: robustly capture n=#### if present (avoid regex precedence bug)
    r.sent_n = _find_int(r"sent_acc\s*:\s*.*?\(n\s*=\s*(\d+)\)", text)

    # per-aspect lines: "- battery : 0.9654"
    for a in ASPECTS:
        val = _find_float(rf"[-–]\s*{re.escape(a)}\s*:\s*({_FLOAT})", text)
        if val is not None:
            r.per_aspect_f1[a] = val

    return r


def ensure_dir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)


def fmt_float(x: Optional[float], nd: int = 4) -> str:
    if x is None:
        return "-"
    return f"{x:.{nd}f}"


def export_csv(results: List[EvalResult], out_dir: Path) -> Path:
    out_path = out_dir / "results_table.csv"
    headers = ["model", "macro_f1", "micro_f1", "sent_acc", "sent_n"] + [f"f1_{a}" for a in ASPECTS]

    with out_path.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(headers)
        for r in results:
            row = [
                r.name,
                r.macro_f1 if r.macro_f1 is not None else "",
                r.micro_f1 if r.micro_f1 is not None else "",
                r.sent_acc if r.sent_acc is not None else "",
                r.sent_n if r.sent_n is not None else "",
            ]
            for a in ASPECTS:
                row.append(r.per_aspect_f1.get(a, ""))
            w.writerow(row)

    return out_path


def export_markdown(results: List[EvalResult], out_dir: Path) -> Path:
    out_path = out_dir / "results_table.md"

    lines: List[str] = []
    lines.append("## Overall metrics (TEST)\n")
    lines.append("| Model | macro_f1 | micro_f1 | sent_acc |")
    lines.append("|---|---:|---:|---:|")
    for r in results:
        lines.append(
            f"| {r.name} | {fmt_float(r.macro_f1)} | {fmt_float(r.micro_f1)} | {fmt_float(r.sent_acc)} |"
        )

    lines.append("\n## Per-aspect F1 (TEST)\n")
    lines.append("| Aspect | " + " | ".join([r.name for r in results]) + " |")
    lines.append("|---|" + "|".join(["---:"] * len(results)) + "|")
    for a in ASPECTS:
        row = [a]
        for r in results:
            row.append(fmt_float(r.per_aspect_f1.get(a)))
        lines.append("| " + " | ".join(row) + " |")

    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return out_path


def export_latex(results: List[EvalResult], out_dir: Path) -> Path:
    """
    Produces IEEE-friendly LaTeX tables.
    """
    out_path = out_dir / "results_table.tex"

    lines: List[str] = []
    lines.append("% Auto-generated by scripts/export_results_table.py\n")

    # Overall latex
    lines.append("\\begin{table}[t]\n\\centering")
    lines.append("\\caption{Overall performance on TEST split.}")
    lines.append("\\label{tab:overall_test}")
    lines.append("\\begin{tabular}{lccc}")
    lines.append("\\hline")
    lines.append("Model & Macro-F1 & Micro-F1 & Sent-Acc \\\\")
    lines.append("\\hline")
    for r in results:
        lines.append(
            f"{r.name} & {fmt_float(r.macro_f1)} & {fmt_float(r.micro_f1)} & {fmt_float(r.sent_acc)} \\\\"
        )
    lines.append("\\hline")
    lines.append("\\end{tabular}")
    lines.append("\\end{table}\n")

    # Per-aspect latex
    lines.append("\\begin{table}[t]\n\\centering")
    lines.append("\\caption{Per-aspect F1 on TEST split.}")
    lines.append("\\label{tab:per_aspect_test}")
    colspec = "l" + "c" * len(results)

    # FIX: escape backslash and braces correctly in f-string
    lines.append(f"\\\\begin{{tabular}}{{{colspec}}}")

    lines.append("\\hline")
    header = "Aspect & " + " & ".join([r.name for r in results]) + " \\\\"
    lines.append(header)
    lines.append("\\hline")
    for a in ASPECTS:
        vals = " & ".join([fmt_float(r.per_aspect_f1.get(a)) for r in results])
        lines.append(f"{a} & {vals} \\\\")
    lines.append("\\hline")
    lines.append("\\end{tabular}")
    lines.append("\\end{table}\n")

    out_path.write_text("\n".join(lines), encoding="utf-8")
    return out_path


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--baseline_log", required=True, help="Path to baseline eval_test.txt")
    ap.add_argument("--single_log", required=True, help="Path to phobert_single eval_test.txt")
    ap.add_argument("--multitask_log", required=True, help="Path to phobert_multitask eval_test.txt")
    ap.add_argument("--out_dir", required=True, help="Output directory")
    args = ap.parse_args()

    baseline_log = Path(args.baseline_log)
    single_log = Path(args.single_log)
    multitask_log = Path(args.multitask_log)
    out_dir = Path(args.out_dir)

    for p in [baseline_log, single_log, multitask_log]:
        if not p.exists():
            raise FileNotFoundError(f"Not found: {p}")

    ensure_dir(out_dir)

    results = [
        parse_eval_log("Baseline", baseline_log),
        parse_eval_log("PhoBERT Single", single_log),
        parse_eval_log("PhoBERT Multi", multitask_log),
    ]

    p_csv = export_csv(results, out_dir)
    p_md = export_markdown(results, out_dir)
    p_tex = export_latex(results, out_dir)

    print("Saved:")
    print(f"- {p_csv}")
    print(f"- {p_md}")
    print(f"- {p_tex}")


if __name__ == "__main__":
    main()
